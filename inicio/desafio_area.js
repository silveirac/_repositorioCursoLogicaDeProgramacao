// Calcular área da circunferência
// acirc = PI * raio * raio

const PI = 3.141592;

let raio = 10;
let areaCirc = PI * raio * raio;
console.log("O valor da área da circunferência de raio 10m é igual a : "
     + areaCirc
     + "m².");

areaCirc = Math.PI * raio * raio;
console.log("O valor da área da circunferência de raio 10m é igual a : "
     + areaCirc
     + "m².");
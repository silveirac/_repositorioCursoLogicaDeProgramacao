//2 params -> console.log(+)
//2 params -> console.log(-)

let numA1 = 17;
let numA2 = 8;
let numB1 = 25;
let numB2 = 3;


function sum(n1, n2) {
    console.log(`${n1} + ${n2} = ${n1 + n2}`);
}

function subt(n1, n2) {
    console.log(`${n1} - ${n2} = ${n1 - n2}`);
}

sum(numA1, numA2);
sum(numB1, numB2);
subt(numA1, numA2);
subt(numB1, numB2);



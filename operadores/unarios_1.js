let a = 1;
a = a + 1; // sintaxe tipo infix: operador "+" fica ENTRE os dois operandos "a" e "1"
a += 1;
a++; // operador postfix: operador "+" fica DEPOIS do operando "a"
++a; // operador prefix: operador "+" fica ANTES do operando "a"

console.log(a);

a = a - 1;
a -= 1;
a--;
--a;

console.log(a);